package com.java.dto;

public class ManagerVO {

	private String manager_code;
	private String manager_name;
	private String manager_phone_num;
	private String manager_pwd;
	public String getManager_code() {
		return manager_code;
	}
	public void setManager_code(String manager_code) {
		this.manager_code = manager_code;
	}
	public String getManager_name() {
		return manager_name;
	}
	public void setManager_name(String manager_name) {
		this.manager_name = manager_name;
	}
	public String getManager_phone_num() {
		return manager_phone_num;
	}
	public void setManager_phone_num(String manager_phone_num) {
		this.manager_phone_num = manager_phone_num;
	}
	public String getManager_pwd() {
		return manager_pwd;
	}
	public void setManager_pwd(String manager_pwd) {
		this.manager_pwd = manager_pwd;
	}
	
}


	